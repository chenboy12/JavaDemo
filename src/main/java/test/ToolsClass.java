package test;

//Java常用工具类
public class ToolsClass {
    //Math工具类
    public void MathTool(){
        //求绝对值
        Math.abs(-100);//100
        Math.abs(30);//30

        //求最大值，最小值
        Math.min(12,18);//
        Math.max(12,18);

        //计算x^y次方
        Math.pow(2,10);

        //计算√x
        Math.sqrt(2);

        //计算e^x次方
        Math.exp(10);

        //计算以e为底的对数
        Math.log(2);
    }
}
